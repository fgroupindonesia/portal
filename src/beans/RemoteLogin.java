package beans;

/**
 *
 * @author ASUS
 */
public class RemoteLogin {

    private int id;
    private String username;
    private String machine_unique;
    private String country;
    private String region;
    private String city;
    private String isp;
    private String isp_as;
    private String ip_address;
    private String status;
    private String date_created;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the machine_unique
     */
    public String getMachine_unique() {
        return machine_unique;
    }

    /**
     * @param machine_unique the machine_unique to set
     */
    public void setMachine_unique(String machine_unique) {
        this.machine_unique = machine_unique;
    }

    /**
     * @return the country
     */
    public String getCountry() {
        return country;
    }

    /**
     * @param country the country to set
     */
    public void setCountry(String country) {
        this.country = country;
    }

    /**
     * @return the region
     */
    public String getRegion() {
        return region;
    }

    /**
     * @param region the region to set
     */
    public void setRegion(String region) {
        this.region = region;
    }

    /**
     * @return the city
     */
    public String getCity() {
        return city;
    }

    /**
     * @param city the city to set
     */
    public void setCity(String city) {
        this.city = city;
    }

    /**
     * @return the isp
     */
    public String getIsp() {
        return isp;
    }

    /**
     * @param isp the isp to set
     */
    public void setIsp(String isp) {
        this.isp = isp;
    }

    /**
     * @return the isp_as
     */
    public String getIsp_as() {
        return isp_as;
    }

    /**
     * @param isp_as the isp_as to set
     */
    public void setIsp_as(String isp_as) {
        this.isp_as = isp_as;
    }

    /**
     * @return the ip_address
     */
    public String getIp_address() {
        return ip_address;
    }

    /**
     * @param ip_address the ip_address to set
     */
    public void setIp_address(String ip_address) {
        this.ip_address = ip_address;
    }

    /**
     * @return the status
     */
    public String getStatus() {
        return status;
    }

    /**
     * @param status the status to set
     */
    public void setStatus(String status) {
        this.status = status;
    }

    /**
     * @return the date_created
     */
    public String getDate_created() {
        return date_created;
    }

    /**
     * @param date_created the date_created to set
     */
    public void setDate_created(String date_created) {
        this.date_created = date_created;
    }
            
    
}
